// Policy templates — the deterministic content for the §314.4(c) written policies (PRD #22).
// Pure and dependency-light (mirrors shared/incident-response.ts / security-architecture.ts): no
// LLM, no DB, no network, no dates. Imported by both runtimes and the client, so imports are
// bare, runtime-neutral, and explicitly `.ts`.
//
// DETERMINISM / GROUNDING (the compliance non-negotiable): each policy's clauses are authored,
// prescriptive statements — what the policy REQUIRES — headed by the §314.4(c) subsection the
// policy fulfills. The "current posture" is derived deterministically from the dealer's saved
// answers to the related questionnaire controls; it reports each control's HONEST status (with its
// own §314.4 citation + triggering answer) and NEVER claims a control is in place when the answer
// says otherwise. No clause, citation, or status is generated by an LLM.

