export interface PolicyPostureItem {
  requirement: Requirement;
  status: ControlStatus;
  triggeringAnswer: AnswerValue;
  isGap: boolean;
}

export interface PolicyPosture {
  confirmed: PolicyPostureItem[];
  gaps: PolicyPostureItem[];
}

/** The dealer's saved answer for a control, phrased for the reader (grounded in the derived
 *  status). Shared by the plaintext and PDF renderings so the wording can never drift. */
export function policyAnswerLabel(status: ControlStatus): string {
  if (status === 'implemented') return 'Current answer: Yes';
  if (status === 'partial') return 'Current answer: Partially in place';
  if (status === 'not_implemented') return 'Current answer: No';
  return 'Not answered yet';
}

/**
 * Derive the honest current posture for a policy from the saved answers. Pure: joins this policy's
 * grounding codes with the global catalog + the answer map, splitting into controls confirmed in
 * place vs. open gaps. Never guesses an unanswered control as a negative claim — an `unknown`
 * status is an open item ("Not answered yet"), not a false "implemented".
 */
export function buildPolicyPosture(
  def: PolicyDefinition,
  answers: Record<string, AnswerValue> | null | undefined,
): PolicyPosture {
  const catalog = REQUIREMENT_CATALOG.filter((r) => def.groundingCodes.includes(r.code));
  const items: PolicyPostureItem[] = joinControlsWithRequirements(catalog, answers).map((c) => ({
    requirement: c.requirement,
    status: c.status,
    triggeringAnswer: c.triggeringAnswer,
    isGap: isGapStatus(c.status),
  }));
  return {
    confirmed: items.filter((i) => !i.isGap),
    gaps: items.filter((i) => i.isGap),
  };
}

/** Minimal structural dealership shape for the plaintext rendering. */
export interface PolicyDealership {
  name: string;
  qualifiedIndividual: string;
  qiEmail: string;
}

/**
 * Render the policy to plain text — the authoritative human-readable content stored on the
 * `policies` row (`content`). Deterministic; the PDF renders the same clauses + posture. Cites the
 * requirement and reflects the dealer's honest posture, so it is safe to store and to unit-test.
 */
export function renderPolicyText(
  def: PolicyDefinition,
  dealership: PolicyDealership,
  answers: Record<string, AnswerValue> | null | undefined,
): string {
  const posture = buildPolicyPosture(def, answers);
  const lines: string[] = [];
  lines.push(def.title.toUpperCase());
  lines.push(`FTC Safeguards Rule ${def.citation} — 16 CFR Part 314`);
  lines.push('');
  lines.push(`Dealership: ${dealership.name || 'Not provided'}`);
  lines.push(
    `Qualified Individual: ${dealership.qualifiedIndividual || 'Not designated'}${
      dealership.qiEmail ? ` (${dealership.qiEmail})` : ''
    }`,
  );
  lines.push('Status: DRAFT — review with counsel and formally adopt before relying on this policy.');
  lines.push('');
  lines.push('PURPOSE');
  lines.push(def.purpose);
  lines.push('');
  lines.push(`This policy implements the safeguard required by 16 CFR ${def.citation}.`);
  lines.push('');
  lines.push('POLICY STATEMENTS');
  def.clauses.forEach((clause, i) => {
    lines.push(`${i + 1}. ${clause.heading}`);
    lines.push(`   ${clause.body}`);
  });
  lines.push('');
  lines.push(`CURRENT POSTURE (${def.citation})`);
  if (posture.confirmed.length === 0 && posture.gaps.length === 0) {
    lines.push('No related self-assessment answers were found for this control area.');
  } else {
    if (posture.confirmed.length > 0) {
      lines.push('Confirmed in place:');
      for (const item of posture.confirmed) {
        lines.push(`- ${item.requirement.title} [${item.requirement.citation}] — ${policyAnswerLabel(item.status)}`);
      }
    }
    if (posture.gaps.length > 0) {
      lines.push('Required — not yet confirmed in place:');
      for (const item of posture.gaps) {
        lines.push(
          `- ${item.requirement.title} [${item.requirement.citation}] — ${policyAnswerLabel(item.status)}`,
        );
      }
      lines.push(
        'These controls are required by the Safeguards Rule but are not yet confirmed in place. Adopting ' +
          'this policy is the first step; implement and evidence each item to close the gap.',
      );
    }
  }
  lines.push('');
  lines.push('DISCLAIMER');
  lines.push(ASSESSMENT_DISCLAIMER);
  return lines.join('\n');
}
