export interface PolicyClause {
  heading: string;
  body: string;
}

export interface PolicyDefinition {
  policyType: PolicyType;
  title: string;
  /** The §314.4(c) subsection this policy fulfills (grounded in the Rule text). */
  citation: string;
  /** generated_documents.docType for this policy's PDF. */
  docType: string;
  /** One-line statement of what the policy governs. */
  purpose: string;
  /** Primary questionnaire code used to link the policies row's requirementId. Undefined when the
   *  Rule subsection has no direct questionnaire control (change management, §314.4(c)(7)). */
  primaryCode?: string;
  /** Questionnaire codes whose saved answers evidence the dealer's honest current posture. */
  groundingCodes: string[];
  /** Authored, normative policy statements (deterministic — NOT generated). */
  clauses: PolicyClause[];
}

